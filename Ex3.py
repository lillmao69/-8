def compound_interest(principal, rate, time):
    # Формула для расчета сложного процента: A = P * (1 + r/n)^(nt)
    # Где:
    # A - итоговая сумма
    # P - начальная сумма (принципал)
    # r - процентная ставка (в десятичной форме)
    # n - количество раз, когда проценты начисляются за год
    # t - количество лет

    # В данной программе будем считать, что проценты начисляются ежегодно, поэтому n = 1
    n = 1
    amount = principal * (1 + rate / n) ** (n * time)
    return amount

# Ввод данных пользователем
principal = float(input("Введите начальную сумму: "))
rate = float(input("Введите процентную ставку (в виде десятичной дроби): "))
time = int(input("Введите количество лет: "))

# Вызов функции и вывод результата
result = compound_interest(principal, rate, time)
print(f"Через {time} лет ваша инвестиция вырастет до: {result:.2f}")
